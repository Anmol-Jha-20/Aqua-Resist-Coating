    <h2 style="color:#57B33E; margin-top:40px;">What is an Auto Transfer Switch (ATS)?</h2>
    <p>An Auto Transfer Switch (ATS) automatically switches power from the main supply to a backup generator when a power outage occurs. Unlike manual switches, it operates automatically to prevent downtime and ensure critical systems continue running.</p>

